        SqlConnection sqlcon = new SqlConnection(DapperORM.connectionString);
        DynamicParameters param = new DynamicParameters();
        // GET: Setting/Setting_TimeOffice_LocationRegistration
        #region LocationRegistration
        public ActionResult Setting_TimeOffice_LocationRegistration(string LocationRegistrationId_Encrypted, int? CmpId)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 303;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                ViewBag.AddUpdateTitle = "Add";
                Mas_LocationRegistration MasLocationRegistration = new Mas_LocationRegistration();
                if (LocationRegistrationId_Encrypted != null)
                {
                    DynamicParameters paramList = new DynamicParameters();
                    ViewBag.AddUpdateTitle = "Update";
                    paramList.Add("@p_LocationRegistrationId_Encrypted", LocationRegistrationId_Encrypted);
                    MasLocationRegistration = DapperORM.ReturnList<Mas_LocationRegistration>("sp_List_Mas_LocationRegistration", paramList).FirstOrDefault();

                    TempData["UpdateId"] = LocationRegistrationId_Encrypted;
                    TempData["LocationLatitude"] = MasLocationRegistration.LocationLatitude;
                    TempData["LocationLongitude"] = MasLocationRegistration.LocationLongitude;
                }
                else
                {
                    TempData["UpdateId"] = "";
                    TempData["LocationLatitude"] = "";
                    TempData["LocationLongitude"] = "";
                }
                return View(MasLocationRegistration);
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
        #endregion

        #region MyRegion
        public ActionResult IsLocationRegistrationExists(string LocationName, string LocationRegistrationId_Encrypted)
        {
            try
            {

                using (SqlConnection sqlcon = new SqlConnection(DapperORM.connectionString))
                {
                    param.Add("@p_process", "IsValidation");
                    param.Add("@p_LocationName", LocationName);
                    param.Add("@p_LocationRegistrationId_Encrypted", LocationRegistrationId_Encrypted);
                    param.Add("@p_MachineName", Dns.GetHostName().ToString());
                    param.Add("@p_CreatedUpdateBy", Session["EmployeeName"]);
                    param.Add("@p_msg", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                    param.Add("@p_Icon", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                    var Result = DapperORM.ExecuteReturn("sp_SUD_Mas_LocationRegistration", param);
