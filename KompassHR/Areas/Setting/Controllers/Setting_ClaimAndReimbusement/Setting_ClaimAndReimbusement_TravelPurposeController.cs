        [HttpGet]
        public ActionResult Setting_ClaimAndReimbusement_TravelPurpose(string TravelPurposeId_Encrypted)
        {

            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                // CHECK IF USER HAS ACCESS OR NOT
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 66;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                ViewBag.AddUpdateTitle = "Add";
                Claim_TravelPurpose Claim_TravelPurpose = new Claim_TravelPurpose();
                if (TravelPurposeId_Encrypted != null)
                {
                    ViewBag.AddUpdateTitle = "Update";
                    param.Add("@p_TravelPurposeId_Encrypted", TravelPurposeId_Encrypted);
                    Claim_TravelPurpose = DapperORM.ReturnList<Claim_TravelPurpose>("sp_List_Claim_TravelPurpose", param).FirstOrDefault();
                }

                return View(Claim_TravelPurpose);
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }

