        public ActionResult Setting_ClaimAndReimbusement_ExpenceCategory(string ExpenseCategory_Encrypted)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                // CHECK IF USER HAS ACCESS OR NOT
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 64;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                ViewBag.AddUpdateTitle = "Add";
                Claim_ExpenseCategory Claim_ExpenseCategoryMaster = new Claim_ExpenseCategory();
                if (ExpenseCategory_Encrypted != "")
                {
                    ViewBag.AddUpdateTitle = "Update";
                    param.Add("@p_ExpenseCategoryID_Encrypted", ExpenseCategory_Encrypted);
                    Claim_ExpenseCategoryMaster = DapperORM.ReturnList<Claim_ExpenseCategory>("sp_List_Claim_ExpenseCategory", param).FirstOrDefault();
                }

                return View(Claim_ExpenseCategoryMaster);
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
