        }
        #endregion

        #region Delete
        public ActionResult Delete(string EmployeeLevelID_Encrypted)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
