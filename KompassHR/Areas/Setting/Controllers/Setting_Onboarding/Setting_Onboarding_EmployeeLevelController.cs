                return RedirectToAction("GetList", "Setting_Onboarding_EmployeeLevel");
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
        #endregion

    }
