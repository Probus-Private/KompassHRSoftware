    public class Setting_Onboarding_BirthdayWorkAnniversaryController : Controller
    {
        DynamicParameters param = new DynamicParameters();
        #region BirthdayWorkAnniversary Main View 
        [HttpGet]
        public ActionResult Setting_Onboarding_BirthdayWorkAnniversary(string SpecialAnniversaryID_Encrypted)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                // CHECK IF USER HAS ACCESS OR NOT
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 33;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                ViewBag.AddUpdateTitle = "Add";
                Mas_SpecialAnniversary mas_specialAnniversary = new Mas_SpecialAnniversary();
                if (SpecialAnniversaryID_Encrypted != null)
                {
                    ViewBag.AddUpdateTitle = "Update";
                    param = new DynamicParameters();
                    param.Add("@p_SpecialAnniversaryID_Encrypted", SpecialAnniversaryID_Encrypted);
                    mas_specialAnniversary = DapperORM.ReturnList<Mas_SpecialAnniversary>("sp_List_Mas_SpecialAnniversary", param).FirstOrDefault();

                }
                return View(mas_specialAnniversary);
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
        #endregion

        #region IsValidation

        [HttpGet]
        public ActionResult IsBirthdayWorkAnniversaryExists(int Year, string SpecialAnniversaryIDEncrypted,string WorkBirth)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                using (SqlConnection sqlcon = new SqlConnection(DapperORM.connectionString))
                {

                    param.Add("@p_process", "IsValidation");
                    param.Add("@p_SpecialAnniversaryID_Encrypted", SpecialAnniversaryIDEncrypted);
                    param.Add("@p_Year", Year);
                    param.Add("@p_WorkBirth",WorkBirth);
                    param.Add("@p_MachineName", Dns.GetHostName().ToString());
                    param.Add("@p_CreatedUpdateBy", Session["EmployeeName"]);
                    param.Add("@p_msg", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                    param.Add("@p_Icon", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                    var Result = DapperORM.ExecuteReturn("sp_SUD_Mas_SpecialAnniversary", param);
                    var Message = param.Get<string>("@p_msg");
                    var Icon = param.Get<string>("@p_Icon");
                    if (Message != "")
                    {
                        return Json(new { Message, Icon }, JsonRequestBehavior.AllowGet);
                    }
                    else
                    {
                        return Json(true, JsonRequestBehavior.AllowGet);
                    }
                }
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
        #endregion

        #region SaveUpdate
        [HttpPost]
        public ActionResult SaveUpdate(Mas_SpecialAnniversary SpecialAnniversary)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                param.Add("@p_process", string.IsNullOrEmpty(SpecialAnniversary.SpecialAnniversaryID_Encrypted) ? "Save" : "Update");
                param.Add("@p_SpecialAnniversaryID", SpecialAnniversary.SpecialAnniversaryID);
                param.Add("@P_SpecialAnniversaryID_Encrypted", SpecialAnniversary.SpecialAnniversaryID_Encrypted);
                param.Add("@p_Year", SpecialAnniversary.Year);
                param.Add("@p_WorkBirth", SpecialAnniversary.WorkBirth);
                param.Add("@p_CreatedUpdateBy", Session["EmployeeName"]);
                param.Add("@p_MachineName", Dns.GetHostName().ToString());
                param.Add("@p_msg", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                param.Add("@p_Icon", dbType: DbType.String, direction: ParameterDirection.Output, size: 500);
                var Result = DapperORM.ExecuteReturn("sp_SUD_Mas_SpecialAnniversary", param);
                TempData["Message"] = param.Get<string>("@p_msg");
                TempData["Icon"] = param.Get<string>("@p_Icon");
                return RedirectToAction("Setting_Onboarding_BirthdayWorkAnniversary", "Setting_Onboarding_BirthdayWorkAnniversary");
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }
        }
        #endregion

