        // GET: TMSSetting/TaskCategory
        public ActionResult Setting_TMS_TaskCategory(string TaskCategoryId_Encrypted)
        {
            try
            {

                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { area = "" });
                }
                // CHECK IF USER HAS ACCESS OR NOT
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 88;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                ViewBag.AddUpdateTitle = "Add";
                TMS_TaskCategory TMS_TaskCategory = new TMS_TaskCategory();
                if (TaskCategoryId_Encrypted != null)
                {
                    ViewBag.AddUpdateTitle = "Update";
                    param.Add("@p_TaskCategoryId_Encrypted", TaskCategoryId_Encrypted);
                    TMS_TaskCategory = DapperORM.ReturnList<TMS_TaskCategory>("sp_List_TMS_TaskCategory", param).FirstOrDefault();
                }
                return View(TMS_TaskCategory);
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }


        }

