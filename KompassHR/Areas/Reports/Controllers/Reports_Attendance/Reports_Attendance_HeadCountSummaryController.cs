    public class Reports_Attendance_HeadCountSummaryController : Controller
    {
        SqlConnection sqlcon = new SqlConnection(DapperORM.connectionString);
        DynamicParameters param = new DynamicParameters();
        GetMenuList ClsGetMenuList = new GetMenuList();
        // GET: Reports/Reports_Attendance_HeadCountSummary
        #region Reports_Attendance_HeadCountSummary
        public ActionResult Reports_Attendance_HeadCountSummary(int? id, int? ScreenId)
        {
            try
            {
                if (Session["EmployeeId"] == null)
                {
                    return RedirectToAction("Login", "Login", new { Area = "" });
                }
                // CHECK IF USER HAS ACCESS OR NOT
                int screenId = Request.QueryString["ScreenId"] != null ? Convert.ToInt32(Request.QueryString["ScreenId"]) : 342;
                bool CheckAccess = new BulkAccessClass().CheckAccess(screenId, Convert.ToInt32(Session["UserAccessPolicyId"]));
                if (!CheckAccess)
                {
                    Session["AccessCheck"] = "False";
                    return RedirectToAction("Dashboard", "Dashboard", new { area = "" });
                }
                if (ScreenId != null)
                {
                    Session["ModuleId"] = id;
                    Session["ScreenId"] = ScreenId;
                    var GetMenuList = ClsGetMenuList.GetMenu(Session["UserAccessPolicyId"].ToString(), id, ScreenId, "InnerSubForm", "Report");
                    ViewBag.GetUserMenuList = GetMenuList;
                }
                else
                {
                    var GetMenuList = ClsGetMenuList.GetMenu(Session["UserAccessPolicyId"].ToString(), Convert.ToInt32(Session["ModuleId"]), Convert.ToInt32(Session["ScreenId"]), "InnerSubForm", "Report");
                    ViewBag.GetUserMenuList = GetMenuList;
                }
                //param.Add("@p_EmployeeId", Session["EmployeeId"]);
                //param.Add("@p_UserRightsModuleId", Session["ModuleId"]);
                //param.Add("@p_Transactiontype", "Report");
                //param.Add("@p_ScreenSubId", ScreenSubId);
                //var GetMenuList = DapperORM.ExecuteSP<dynamic>("sp_GetUserRights_InnerSubMenuList", param).ToList(); // SP_getReportingManager
                //ViewBag.GetUserMenuList = GetMenuList;
                return View();
            }
            catch (Exception ex)
            {
                Session["GetErrorMessage"] = ex.Message;
                return RedirectToAction("ErrorPage", "Login");
            }

        }
        #endregion

    }
}